CREATE DATABASE IF NOT EXISTS drone_db;
USE drone_db;

CREATE TABLE IF NOT EXISTS drone (
    drone_id VARCHAR(50) PRIMARY KEY,
    battery INT NOT NULL DEFAULT 100,
    status VARCHAR(20) NOT NULL DEFAULT 'OPERATIONAL',
    lat DOUBLE NOT NULL,
    lng DOUBLE NOT NULL
);

